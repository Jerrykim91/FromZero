# 컴퓨테이셔널 씽킹과 알고리즘의 차이점은 무엇인가요?

# **컴퓨테이셔널 씽킹은 4가지**
# 분해: 복잡한 문제를 작은 문제로 나눔
# 패턴 인식: 문제 안에서 유사성을 발견
# 추상화: 문제의 핵심에만 집중, 부차적인 것은 제외
# 알고리즘: 이렇게 정의한 문제를 해결하는 절차(일반화와 모델링은 여기에 포함)

# 현실에서 컴퓨터로 해결하려는 문제는 정형화된 문제가 아니라 비정형화된 문제가 더 많다.
